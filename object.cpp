void GameObject::Render() {   
    if (GetType() == "skybox") {
        shader.use();
        glm::mat4 view = glm::mat4(glm::mat3(glm::lookAt(cameraPos, cameraPos + cameraFront, cameraUp)));
        shader.setMat4("view", view);
        glm::mat4 projection = glm::perspective(glm::radians(80.0f), (float)1920 / (float)1080, 0.1f, 100.0f);
        shader.setMat4("projection", projection);

        glDepthFunc(GL_LEQUAL);
        glBindVertexArray(VAO);
        glBindTexture(GL_TEXTURE_CUBE_MAP, texture);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glDepthFunc(GL_LESS);
        return;
    }

    shader.use();
    shader.setVec3("viewPos", glm::vec3(0.0f, 0.0f, 0.0f));
    shader.setVec3("lightPos", glm::vec3(0.0f, 0.0f, 1.0f));


    glm::mat4 view = glm::mat4(glm::mat3(glm::lookAt(cameraPos, cameraPos + cameraFront, cameraUp)));
    shader.setMat4("view", view);

    glm::mat4 projection = glm::perspective(glm::radians(80.0f), (float)1920 / (float)1080, 0.1f, 100.0f);
    shader.setMat4("projection", projection);

    glm::mat4 shaderModel = glm::mat4(1.0f);
    shaderModel = glm::scale(shaderModel, scale);
    shaderModel = glm::translate(shaderModel, position);
    shaderModel = glm::rotate(shaderModel, rotation.x, glm::vec3(-1.0f, 0.0f, 0.0f));
    shaderModel = glm::rotate(shaderModel, rotation.y, glm::vec3(0.0f, 1.0f, 0.0f));
    shader.setMat4("model", shaderModel);

    for (unsigned int i = 0; i < this->model.meshes.size(); i++)
        this->model.meshes[i].Draw(shader);

    return;
}